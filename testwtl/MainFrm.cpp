LRESULT CMainFrame::OnCreate(UINT /*uMsg*/, WPARAM /*wParam*/, LPARAM /*lParam*/, BOOL& /*bHandled*/)
{
	// create command bar window
	HWND hWndCmdBar = m_CmdBar.Create(m_hWnd, rcDefault, NULL, ATL_SIMPLE_CMDBAR_PANE_STYLE);
	// attach menu
	m_CmdBar.AttachMenu(GetMenu());
	// load command bar images
	m_CmdBar.LoadImages(IDR_MAINFRAME);
	// remove old menu
	SetMenu(NULL);

	HWND hWndToolBar = CreateSimpleToolBarCtrl(m_hWnd, IDR_MAINFRAME, FALSE, ATL_SIMPLE_TOOLBAR_PANE_STYLE);

	CreateSimpleReBar(ATL_SIMPLE_REBAR_NOBORDER_STYLE);
	AddSimpleReBarBand(hWndCmdBar);
	AddSimpleReBarBand(hWndToolBar, NULL, TRUE);

	CreateSimpleStatusBar();

//	m_hWndClient = m_view.Create(m_hWnd, rcDefault, NULL, WS_CHILD | WS_VISIBLE | WS_CLIPSIBLINGS | WS_CLIPCHILDREN, WS_EX_CLIENTEDGE);

	HWND hWndView = _cameraView.Create(m_hWnd, rcDefault, NULL, WS_CHILD | WS_VISIBLE | WS_CLIPSIBLINGS | WS_CLIPCHILDREN, WS_EX_CLIENTEDGE);
	
	UIAddToolBar(hWndToolBar);
	UISetCheck(ID_VIEW_TOOLBAR, 1);
	UISetCheck(ID_VIEW_STATUS_BAR, 1);

	// register object for message filtering and idle updates
	CMessageLoop* pLoop = _Module.GetMessageLoop();
	ATLASSERT(pLoop != NULL);
	pLoop->AddMessageFilter(this);
	pLoop->AddIdleHandler(this);

	_capture.setWindowHandle(hWndView);
	//_texture.loadFromFile("image1.jpg");
	//_test.setTexture(_texture, true);
	//_test.setPosition(100, 100);
	//_border.setFillColor(sf::Color(0,0,0));
	//_border.setOutlineColor(sf::Color(0, 255, 0));
	//_border.setOutlineThickness(5.0f);
	assert(_capture.initCamera());
	_capture.setHDRMode(true);
	_capture.StartCapture();
	
	//SetTimer(IDR_UPDATETIMER, 1000);
	_capture.setRawTextureCallback([this](uint32_t width, uint32_t height, const std::vector<uint8_t>& pixels) {
		if (width > 0 && height > 0) {
			sf::Vector2u size(width, height);
			_cameraView.lock();
			auto& texture = _cameraView.getTexture();
			if (size != texture.getSize()) assert(texture.create(width, height));
			texture.update(pixels.data());
			_cameraView.unlock();
			_cameraView.Invalidate();
		}
	});
	return 0;
}

LRESULT CMainFrame::OnDestroy(UINT /*uMsg*/, WPARAM /*wParam*/, LPARAM /*lParam*/, BOOL& bHandled)
{
	_capture.StopCapture();
	_capture.setWindowHandle(NULL);
	
	// unregister message filtering and idle updates
	CMessageLoop* pLoop = _Module.GetMessageLoop();
	ATLASSERT(pLoop != NULL);
	pLoop->RemoveMessageFilter(this);
	pLoop->RemoveIdleHandler(this);

	bHandled = FALSE;
	return 1;
}

